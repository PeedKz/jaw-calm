// Get the fact for a specific day (cycles through 30 days)
export function getDailyFact(date: Date = new Date()): DailyFact {
  const startOfYear = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - startOfYear.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  const factIndex = dayOfYear % dailyFacts.length;
  return dailyFacts[factIndex];
}

// Get today's fact number (1-30)
export function getTodayFactNumber(date: Date = new Date()): number {
  const startOfYear = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - startOfYear.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  return (dayOfYear % dailyFacts.length) + 1;
}
